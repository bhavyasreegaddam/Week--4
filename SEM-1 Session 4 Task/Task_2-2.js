//String Example
let a="Hello";
console.log(a);
console.log(typeof(a));

//Number Example
let b=19;
console.log(b);
console.log(typeof(b));

//Boolean Example
let c=true;
console.log(c);
console.log(typeof(c));

//Null Example
let e=null;
console.log(e);
console.log(typeof(e));

//Undefined Example
let d;
console.log(d);
console.log(typeof(d));

//Symbol Example
const x=Symbol();
console.log(x);
console.log(typeof(x));