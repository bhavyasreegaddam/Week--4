//Logical Operators

//&& Operator
let a=10;
let b=20;
if(a==b && a<b){
    console.log("True");
}
else{
    console.log("False");
}

// || operator
let x=10;
let y=20;
if(x==y || x<y){
    console.log("True");
}
else{
    console.log("False");
}

// ! operator
let c = null;
if (!c) {
  console.log("True");  // Output: True
} else {
  console.log("False");
}